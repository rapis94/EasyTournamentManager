    <h1>Sobre mí</h1>
¡Hola! Soy Mario Ovalle, apasionado caster de Age of Empires y enamorado de la estrategia en tiempo real. Desde que descubrí este juego, me cautivó no solo su profundidad histórica y su jugabilidad desafiante, sino también la increíble comunidad que lo rodea. Con el tiempo, encontré en el casting una manera perfecta de combinar dos cosas que me apasionan: comunicar y vivir cada partida al máximo.
<br>
<br>
Me encanta narrar partidas con emoción, detalle y un toque de humor. Mi objetivo es siempre acercar el juego tanto a los veteranos como a los nuevos jugadores, explicando estrategias, comentando movimientos clave y transmitiendo esa adrenalina que solo una buena partida de AoE puede generar.
<br>
<br>

He tenido la suerte de castear torneos importantes, colaborar con grandes figuras de la comunidad y seguir creciendo día a día gracias al apoyo de todos ustedes. Para mí, cada partida es una historia que merece ser contada, y me siento afortunado de poder ser la voz que la acompaña.
</div>